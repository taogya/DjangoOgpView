import locale
import re
from mimetypes import common_types, types_map

from django.db import models

locales = \
    sorted(set(
        [
            code[:code.index('.')] if '.' in code else code
            for code in locale.locale_alias.values()
        ]
    ))
mimetypes = \
    sorted(set(
        list(types_map.values()) + list(common_types.values())
    ))


def _print_locale_enum():
    print('class LocaleCode(Enum):')
    for code in locales:
        print(f'    {code} = \'{code}\'')


def _print_mimetypes_enum():
    print('class MimeType(Enum):')
    for mimetype in mimetypes:
        key = re.sub('[\.\-\+/]', '_', mimetype)
        print(f'    {key} = \'{mimetype}\'')


