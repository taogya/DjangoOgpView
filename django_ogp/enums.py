class OgpType(models.TextChoices):
    music_song = 'music.song'
    music_album = 'music.album'
    music_playlist = 'music.playlist'
    music_radio_station = 'music.radio_station'
    video_movie = 'video.movie'
    video_episode = 'video.episode'
    video_tv_show = 'video.tv_show'
    video_other = 'video.other'
    article = 'article'
    book = 'book'
    profile = 'profile'
    website = 'website'

    @classmethod
    def get_namespace(cls, value: str):
        prefix = {
            'music': 'https://ogp.me/ns/music#',
            'video': 'https://ogp.me/ns/video#',
            'article': 'https://ogp.me/ns/article#',
            'book': 'https://ogp.me/ns/book#',
            'profile': 'https://ogp.me/ns/profile#',
            'website': 'https://ogp.me/ns/website#',
        }
        key = value.split('.')[0]
        return f'{key}: {prefix[key]}'

    @property
    def namespace(self):
        return self.get_namespace(self.value)


